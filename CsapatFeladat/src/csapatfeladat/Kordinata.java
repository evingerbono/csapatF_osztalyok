package csapatfeladat;

public class Kordinata {

    private int x, y;

    public Kordinata(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public Kordinata() {
        this.x = 0;
        this.y = 0;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
